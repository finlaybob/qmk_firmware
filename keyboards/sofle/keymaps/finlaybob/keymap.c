void enc_update_default(uint8_t index, bool cw){
    if (index == 0) {
        if (cw) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    } else if (index == 1) {
        if (cw) {
            tap_code(KC_DOWN);
        } else {
            tap_code(KC_UP);
        }
    }
}

void enc_update_shifted(uint8_t index, bool cw){
    if (index == 0) {
        if (cw) {
            tap_code(C(KC_TAB));
        } else {
            tap_code(C(S(KC_TAB)));
        }
    } else if (index == 1) {
        if (cw) {
            tap_code(KC_RIGHT);
        } else {
            tap_code(KC_LEFT);

        }
    }
}

void encoder_update_user(uint8_t index, bool clockwise)
{

    //bool isShifted = get_mods() & MOD_MASK_CTRL;

    switch (get_highest_layer(layer_state))
    {
        case _RAISE:
        case _LOWER:
            enc_update_shifted(index,clockwise);
            break;
        default:
            enc_update_default(index,clockwise);
    }



}

