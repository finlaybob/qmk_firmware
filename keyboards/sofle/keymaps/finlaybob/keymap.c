static void print_status_narrow(void) {
    // Print current mode

    if (keymap_config.swap_lctl_lgui)
    {
        oled_write_ln_P(PSTR("MACOS"), false);
    }
    else
    {
        oled_write_ln_P(win_logo1, false);
    }

    switch (get_highest_layer(default_layer_state)) {
        case _DEFAULT:
            oled_write_ln_P(PSTR("Norml"), false);
            break;
        case _GAME:
            oled_write_ln_P(PSTR("Games"), false);
            break;
        default:
            oled_write_P(PSTR("Undef"), false);
    }
    oled_write_P(PSTR("-----"), false);
    // Print current layer
    oled_write_P(PSTR("Layer"), false);
    switch (get_highest_layer(layer_state))
    {
        case _GAME:
        case _DEFAULT:
            oled_write_P(PSTR("     "), false);
            oled_write_P(PSTR(" Def "), false);
            oled_write_P(PSTR("     "), false);
            break;
        case _RAISE:
            oled_write_ln_P(up_layer, false);
            break;
        case _LOWER:
            oled_write_ln_P(down_layer, false);
            break;
        case _ADJUST:
            oled_write_ln_P(adj_layer, false);
            break;
        default:
            oled_write_P(PSTR("Undef"), false);
    }
    oled_write_P(PSTR("-----"), false);

    led_t led_usb_state = host_keyboard_led_state();
    oled_write_P(PSTR(" CAP "), led_usb_state.caps_lock);
    oled_write_P(PSTR("\n"), false);

}

